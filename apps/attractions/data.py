def get_landmark_icon(landmark_name):
    lower = landmark_name.lower()
    if any(word in lower for word in ['lake', 'waterfall', 'springs', 'reservoir']):
        return 'waves'
    elif any(word in lower for word in ['park', 'forest', 'valley', 'gorge']):
        return 'tree-pine'
    elif any(word in lower for word in ['mountain', 'peak', 'ridge', 'base camp']):
        return 'mountain'
    elif any(word in lower for word in ['tower', 'fortress', 'archaeological', 'ruins', 'museum', 'caravanserai']):
        return 'landmark'
    elif any(word in lower for word in ['city', 'square', 'bazaar', 'boulevard']):
        return 'building'
    else:
        return 'map-pin'
