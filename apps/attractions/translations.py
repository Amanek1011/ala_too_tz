def get_supported_language(lang):
    return lang if lang in SUPPORTED_LANGUAGES else "ru"


def get_ui_texts(lang):
    code = get_supported_language(lang)
    return {**TEXTS[code], **FEATURE_TEXTS.get(code, {})}


def text(lang, key, **kwargs):
    value = get_ui_texts(lang)[key]
    return value.format(**kwargs) if kwargs else value


def review_word(lang, count):
    ui = get_ui_texts(lang)
    if lang == "en":
        return ui["review_single"] if count == 1 else ui["review_plural"]
    if lang == "ru":
        return ui["review_single"] if count == 1 else ui["review_plural"]
    return ui["review_plural"]
